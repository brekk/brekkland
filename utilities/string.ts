export const slug = (x: string): string =>
  pipe(
    toLower,
    replace(/[^a-z0-9\s-]/g, ""),
    replace(/\s+/g, "-"),
    replace(/-+/g, "-"),
    replace(/^-+|-+$/g, ""),
  )(x)
